#include <stdio.h>
#include <stdlib.h>

int main() {
	char a = 'a';
	
	printf("%c\n", a + 1);
	printf("%i\n", a + 1);

	printf("%c\n", a + 2);
	printf("%i\n", a + 2);
	
	printf("%c\n", a + 28);
	printf("%i\n", a + 28);

	printf("%c\n", a - 300);
	printf("%i\n", a - 300);
	
	printf("%c\n", a * 30);
	printf("%i\n", a * 30);
	
	printf("%c\n", a * 3);
	printf("%i\n", a * 3);

	printf("%c\n", a / 3);
	printf("%i\n", a / 3);
	
	return 0;

}
